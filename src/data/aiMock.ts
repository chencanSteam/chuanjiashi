export interface QuotaItem {
  used: number;
  total: number;
}

export interface AIQuota {
  plan: string;
  interviewQuestion: QuotaItem;
  followUp: QuotaItem;
  biographyGenerate: QuotaItem;
  digitalDialog: QuotaItem;
  storage: { usedMB: number; totalMB: number };
}

const QUOTA_KEY = 'cj_ai_quota';

export const defaultQuota: AIQuota = {
  plan: '标准传记包',
  interviewQuestion: { used: 18, total: 30 },
  followUp: { used: 6, total: 10 },
  biographyGenerate: { used: 1, total: 3 },
  digitalDialog: { used: 12, total: 50 },
  storage: { usedMB: 268, totalMB: 1024 },
};

export function loadQuota(): AIQuota {
  try {
    const raw = localStorage.getItem(QUOTA_KEY);
    if (raw) return { ...defaultQuota, ...JSON.parse(raw) };
  } catch {
    // ignore
  }
  return defaultQuota;
}

export function saveQuota(quota: AIQuota) {
  try {
    localStorage.setItem(QUOTA_KEY, JSON.stringify(quota));
  } catch {
    // ignore
  }
}

export function consumeInterviewQuestion(quota: AIQuota): AIQuota {
  const next = {
    ...quota,
    interviewQuestion: { ...quota.interviewQuestion, used: quota.interviewQuestion.used + 1 },
  };
  saveQuota(next);
  return next;
}

export function consumeFollowUp(quota: AIQuota): AIQuota {
  const next = {
    ...quota,
    followUp: { ...quota.followUp, used: quota.followUp.used + 1 },
  };
  saveQuota(next);
  return next;
}

export function consumeBiographyGenerate(quota: AIQuota): AIQuota {
  const next = {
    ...quota,
    biographyGenerate: { ...quota.biographyGenerate, used: quota.biographyGenerate.used + 1 },
  };
  saveQuota(next);
  return next;
}

export function consumeDigitalDialog(quota: AIQuota): AIQuota {
  const next = {
    ...quota,
    digitalDialog: { ...quota.digitalDialog, used: quota.digitalDialog.used + 1 },
  };
  saveQuota(next);
  return next;
}

